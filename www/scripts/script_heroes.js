// Функция для фильтрации избранных (исправленная)
async function filterFavorites() {
    console.log('=== ФИЛЬТРАЦИЯ ИЗБРАННЫХ ===');
    
    // Проверяем авторизацию через наш новый authService
    if (!window.authService || !window.authService.isAuthenticated()) {
        if (window.showNotification) {
            window.showNotification('Войдите в аккаунт чтобы видеть избранных героев!');
        }
        const loginBtn = document.getElementById('openLoginBtn');
        if (loginBtn) loginBtn.click();
        return false;
    }
    
    // Используем favoritesManager из config.js
    if (window.favoritesManager) {
        try {
            console.log('Загружаем избранные...');
            await window.favoritesManager.loadFavoritesFromDB();
            
            const favorites = window.favoritesManager.favorites;
            console.log('Получены избранные ID:', [...favorites]);
            
            // Показываем/скрываем карточки
            let shownCount = 0;
            const heroCards = document.querySelectorAll('.hero-card');
            
            heroCards.forEach(card => {
                const heroId = card.getAttribute('data-hero');
                const heroDbId = getHeroDbIdFromName(heroId);
                
                if (favorites.has(heroDbId)) {
                    card.style.display = 'block';
                    shownCount++;
                    console.log(`Показана карточка: ${heroId} (ID: ${heroDbId})`);
                } else {
                    card.style.display = 'none';
                }
            });
            
            console.log(`Показано избранных: ${shownCount}`);
            
            return shownCount > 0;
            
        } catch (error) {
            console.error('Ошибка фильтрации избранных:', error);
            if (window.showNotification) {
                window.showNotification('Ошибка загрузки избранных');
            }
            return false;
        }
    } else {
        console.error('❌ FavoritesManager не найден!');
        if (window.showNotification) {
            window.showNotification('Система избранного не загружена');
        }
        return false;
    }
}


