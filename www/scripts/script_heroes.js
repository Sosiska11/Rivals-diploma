document.addEventListener('DOMContentLoaded', function() {
    console.log('🏠 DOM загружен, инициализация страницы героев');
    
    // Бургер-меню
    const burgerMenu = document.querySelector('.burger-menu');
    const nav = document.querySelector('nav');
    if (burgerMenu) {
        burgerMenu.addEventListener('click', function() {
            this.classList.toggle('active');
            nav.classList.toggle('active');
            document.body.style.overflow = nav.classList.contains('active') ? 'hidden' : '';
        });
    }

    // Header scroll effect
    const header = document.getElementById('mainHeader');
    if (header) {
        window.addEventListener('scroll', function() {
            if (window.scrollY > 50) {
                header.classList.add('scrolled');
            } else {
                header.classList.remove('scrolled');
            }
        });
    }

    // Инициализация систем
    initHeroModals();
    
    // Настройка фильтров
    const filterBtns = document.querySelectorAll('.filter-btn');
    const heroCards = document.querySelectorAll('.hero-card');
    
    console.log(`Найдено кнопок фильтра: ${filterBtns.length}`);
    console.log(`Найдено карточек: ${heroCards.length}`);
    
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
                observer.unobserve(entry.target);
            }
        });
    }, {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    });

    // Назначаем обработчики фильтров
    filterBtns.forEach(btn => {
        btn.addEventListener('click', async function() {
            console.log(`Нажата кнопка фильтра: ${this.dataset.filter}`);
            
            // Убираем активный класс со всех кнопок
            filterBtns.forEach(b => b.classList.remove('active'));
            
            // Добавляем активный класс текущей кнопке
            this.classList.add('active');
            
            const filter = this.dataset.filter;
            
            // Если выбраны избранные
            if (filter === 'favorites') {
                await filterFavorites();
                return;
            }
            
            // Обычная фильтрация по ролям
            console.log(`Фильтр: ${filter}`);
            
            heroCards.forEach(card => {
                const category = card.getAttribute('data-category');
                
                if (filter === 'all') {
                    card.style.display = 'block';
                } else if (category === filter) {
                    card.style.display = 'block';
                } else {
                    card.style.display = 'none';
                }
            });
            
            // Переинициализация observer
            heroCards.forEach(card => {
                if (card.style.display !== 'none') {
                    observer.observe(card);
                }
            });
        });
    });

    // Начальная инициализация observer
    heroCards.forEach(card => {
        observer.observe(card);
    });

    // Даем время на загрузку config.js
    setTimeout(() => {
        // Обновляем UI авторизации
        if (window.authService) {
            window.authService.updateAuthUI();
        }
        
        // Добавляем кнопки избранного если менеджер доступен
        if (window.favoritesManager) {
            console.log('🎨 Добавляем кнопки избранного...');
            window.favoritesManager.addFavoriteButtons();
            
            // Загружаем избранные если пользователь авторизован
            if (window.authService && window.authService.isAuthenticated()) {
                setTimeout(() => {
                    window.favoritesManager.loadFavoritesFromDB();
                }, 1500);
            }
        }
    }, 1000);

    console.log('✅ Страница героев инициализирована');
});